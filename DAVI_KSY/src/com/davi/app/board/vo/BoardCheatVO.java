package com.davi.app.board.vo;

public class BoardCheatVO {
	
	private int cheatNum;
	private String cheatHead;
	private String memberId;
	private String cheatTitle;
	private String cheatContent;
	private String cheatDate;
	public int getCheatNum() {
		return cheatNum;
	}
	public void setCheatNum(int cheatNum) {
		this.cheatNum = cheatNum;
	}
	public String getCheatHead() {
		return cheatHead;
	}
	public void setCheatHead(String cheatHead) {
		this.cheatHead = cheatHead;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getCheatTitle() {
		return cheatTitle;
	}
	public void setCheatTitle(String cheatTitle) {
		this.cheatTitle = cheatTitle;
	}
	public String getCheatContent() {
		return cheatContent;
	}
	public void setCheatContent(String cheatContent) {
		this.cheatContent = cheatContent;
	}
	public String getCheatDate() {
		return cheatDate;
	}
	public void setCheatDate(String cheatDate) {
		this.cheatDate = cheatDate;
	}	
	
	
	
}
