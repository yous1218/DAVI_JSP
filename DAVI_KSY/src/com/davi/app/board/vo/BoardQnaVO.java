package com.davi.app.board.vo;

public class BoardQnaVO {
	
	private int qnaNum;
	private String qnaHead;
	private String memberId;
	private String qnaTitle;
	private String qnaContent;
	private String qnaDate;
	private int qnaNotice;
	private String qnaPassword;
	
	public BoardQnaVO() {;	}
	
	public String getQnaDate() {
		return qnaDate;
	}
	public void setQnaDate(String qnaDate) {
		this.qnaDate = qnaDate;
	}
	public int getQnaNotice() {
		return qnaNotice;
	}
	public void setQnaNotice(int qnaNotice) {
		this.qnaNotice = qnaNotice;
	}
	public int getQnaNum() {
		return qnaNum;
	}
	public void setQnaNum(int qnaNum) {
		this.qnaNum = qnaNum;
	}
	public String getQnaHead() {
		return qnaHead;
	}
	public void setQnaHead(String qnaHead) {
		this.qnaHead = qnaHead;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getQnaTitle() {
		return qnaTitle;
	}
	public void setQnaTitle(String qnaTitle) {
		this.qnaTitle = qnaTitle;
	}
	public String getQnaContent() {
		return qnaContent;
	}
	public void setQnaContent(String qnaContent) {
		this.qnaContent = qnaContent;
	}
	public String getQnaPassword() {
		return qnaPassword;
	}
	public void setQnaPassword(String qnaPassword) {
		this.qnaPassword = qnaPassword;
	}

}
